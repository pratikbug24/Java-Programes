class SubOperater 
{
	public static void main(String[] args) 
	{
	    byte a1 = 10;
		byte a2 = 20;
		 
		int sum = a2 - a1;
		
		System.out.println("byte subtraction is  :" + sum);
	
		short s1=200;
		short s2=500;
		
		int s = s2 - s1;
		System.out.println("short subtraction is  :" + s);
		
		char c1 = 'a';
		char c2 = 'z';
		
		int c = c2 - c1;
		
		System.out.println("charecter subtraction is  :" + c);
		
		int i1 = 1000;
		int i2 = 4000;
		
		int i = i2 - i1;
		
		System.out.println("integer subtraction is  :" + i);
		
		long l1= 150l;
		long l2 = 350;
		
		long l = l2 - l1;
	
		System.out.println("long subtraction is  :" + l);
		
		float f1= 1.1f;
		float f2= 2.4f;
		
		float f= f2 - f1;
		System.out.println("float subtraction is :" + f);
		
		double d1= 1.1;
		double d2 = 2.4;
		
		double d= d2 - d1;
		
		System.out.println("double subtraction is :" + d);
		
		String st1 = "Pratik";
		String st2 = "Pawar";
		
		String st = st1 + st2;
		System.out.println("String subtraction is :" + st);
	
	}
}
